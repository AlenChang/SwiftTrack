    public LiveData<Bitmap> getLiveBitmapData() {return bitmap;}

    static public void draw(double[] data) {
        Bitmap tmp = bitmap.getValue();
//        for(int j = 0; j < height - 1; j++){
//            for (int i = 0; i < width; i++){
//                tmp.setPixel(i, j, tmp.getPixel(i, j+1));
//            }
//        }

        for (int i = 0; i < width; i++){
            int c = (int) Math.round(data[i] * 2000);
            if(c > 255){
                c = 255;
            }
            int[] colors = getCmap(c);
            tmp.setPixel(i, iter, Color.rgb(colors[0], colors[1], colors[2]));
        }
        iter++;
        iter = iter % height;

        for (int i = 0; i < width; i++){
            tmp.setPixel(i, iter, Color.rgb(255, 0, 0));
            tmp.setPixel(i, (iter + 1) % height, Color.rgb(255, 0, 0));
            tmp.setPixel(i, (iter + 2) % height, Color.rgb(255, 0, 0));
            tmp.setPixel(i, (iter + 3) % height, Color.rgb(255, 0, 0));
        }

        bitmap.postValue(tmp);

    }

